package cap4_ej07;

public class Numeros {
    
    protected static void Comparar(double A, double B){
        if (A > B){
            System.out.println("\nA es mayor que B");    
        } else if (A < B){
            System.out.println("\nA es menor que B");
        } else{
            System.out.println("\nA es igual que B");
        }
    }

}
